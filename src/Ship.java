
public class Ship {

	private String name;
	private int holes;

	public Ship(String name, int holes){
		this.name = name;
		this.holes = holes;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHoles() {
		return holes;
	}
	public void setHoles(int holes) {
		this.holes = holes;
	}

}
